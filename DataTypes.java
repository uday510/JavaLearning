public class DataTypes {
    /**
     * Data types
     * 1. int -> primitive data type
     * 2. String -> non-primitive data type
     * 3. char -> primitive data type
     * 4. boolean -> primitive data type
     * 5. float -> primitive data type
     * 6. double -> primitive data type
     * 7. long -> primitive data type
     * 8. short -> primitive data type
     * 9. byte -> primitive data type
     * 10. Integer -> non-primitive data type
     * 11. Character -> non-primitive data type
     * 12. Boolean -> non-primitive data type
     * 13. Float -> non-primitive data type
     * 14. Double -> non-primitive data type
     * 15. Long -> non-primitive data type
     * 16. Short -> non-primitive data type
     * 17. Byte -> non-primitive data type
     * 18. void -> primitive data type
     * 19. null -> primitive data type
     * 20. Object -> non-primitive data type
     * 21. Array -> non-primitive data type
     * 22. List -> non-primitive data type
     * 23. Map -> non-primitive data type
     * 24. Set -> non-primitive data type
     * 25. Queue -> non-primitive data type
     * 26. Stack -> non-primitive data type
     * 27. LinkedList -> non-primitive data type
     * 28. ArrayList -> non-primitive data type
     */


    // Eg. for each data type

    // 1. int
    int a = 0; // int is a primitive data type

    // 2. String
    String s = null; // String default value is null

    // 3. Character
    Character character = null; // Character default value is null

    // 4. boolean
    boolean b = false; // boolean default value is false

    // 5. float
    float f = 0.0f; // float default value is 0.0f

    // 6. double
    double d = 0.0; // double default value is 0.0

    // 7. long
    long l = 0; // long default value is 0

    // 8. short
    short sh = 0; // short default value is 0

    // 9. byte
    byte by = 0; // byte default value is 0

    // 10. Integer
    Integer i = null; // Integer default value is null

    // 11. Character
    Character c = null; // Character default value is null

    // 12. Boolean
    Boolean bool = null; // Boolean default value is null

    // 13. Float
    Float fl = null; // Float default value is null

    // 14. Double
    Double db = null; // Double default value is null

    // 15. Long
    Long lo = null; // Long default value is null



}
